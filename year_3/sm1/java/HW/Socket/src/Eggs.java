public class Eggs {
    private int numEggs;


    public Eggs() {
        numEggs = 0;
    }

    public Eggs(int numEggs) {
        this.numEggs = numEggs;
    }


    public int getNumEggs() {
        return numEggs;
    }

    public void setNumEggs(int numEggs) {
        this.numEggs = numEggs;
    }
}
